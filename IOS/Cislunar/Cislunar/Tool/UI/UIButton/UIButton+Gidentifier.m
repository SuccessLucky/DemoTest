#import "UIButton+Gidentifier.h"

@implementation UIButton (Gidentifier)

- (void)setStrIdentifier:(NSString *)strIdentifier
{
    [self bk_associateCopyOfValue:strIdentifier withKey:@"identifer"];
}

- (NSString *)strIdentifier
{
     return [self bk_associatedValueForKey:@"identifer"];
}

@end
